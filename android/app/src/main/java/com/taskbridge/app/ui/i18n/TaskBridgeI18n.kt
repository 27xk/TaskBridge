val LocalTaskBridgeStrings = staticCompositionLocalOf { zhStrings }
val LocalAppLanguage = staticCompositionLocalOf { AppLanguage.Chinese }

@Composable
fun TaskBridgeLanguageProvider(
    language: AppLanguage,
    content: @Composable () -> Unit,
) {
    CompositionLocalProvider(
        LocalAppLanguage provides language,
        LocalTaskBridgeStrings provides stringsFor(language),
    ) {
        content()
    }
}

fun stringsFor(language: AppLanguage): TaskBridgeStrings {
    return when (language) {
        AppLanguage.Chinese -> zhStrings
        AppLanguage.English -> enStrings
    }
}

