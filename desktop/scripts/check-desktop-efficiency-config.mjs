import assert from "node:assert/strict";
import { readFile } from "node:fs/promises";
import { resolve } from "node:path";
import {
  extractOpeningTag,
  hasLiteralBooleanAttribute,
  workspaceRoot,
} from "./script-helpers.mjs";

const root = workspaceRoot(import.meta.url);

const [stateSource, appSource, dbSource, mainSource, settingsViewSource, syncRecoveryPanelSource] = await Promise.all([
  readFile(resolve(root, "electron/state.ts"), "utf8"),
  readFile(resolve(root, "src/App.vue"), "utf8"),
  readFile(resolve(root, "electron/db.ts"), "utf8"),
  readFile(resolve(root, "electron/main.ts"), "utf8"),
  readFile(resolve(root, "src/views/SettingsView.vue"), "utf8"),
  readFile(resolve(root, "src/components/settings/SettingsSyncRecoveryPanel.vue"), "utf8"),
]);

assert.match(
  stateSource,
  /if \(sameSettingValue\(this\.get\(key\), value\)\) return;/,
  "settings store must skip unchanged writes",
);
assert.match(
  stateSource,
  /setMany\(values: Partial<T>\): void/,
  "settings store must batch startup normalization writes",
);
assert.match(
  stateSource,
  /if \(!\(key in this\.data\)\) return;/,
  "settings store must skip missing-key deletes",
);
assert.match(
  stateSource,
  /function sameSettingValue\(current: unknown, next: unknown\): boolean/,
  "settings store must centralize setting value equality",
);
assert.match(
  stateSource,
  /JSON\.stringify\(current\) === JSON\.stringify\(next\)/,
  "settings store must compare plain object settings structurally",
);
assert.match(
  stateSource,
  /settingsSchemaVersion\?: number;/,
  "settings schema must carry an explicit version marker",
);
assert.match(
  stateSource,
  /const CURRENT_SETTINGS_SCHEMA_VERSION = 4;/,
  "settings schema version must be bumped for workspace ownership migration",
);
assert.match(stateSource, /initializeLegacyWorkspaceOwnership\(previousSettingsSchemaVersion\);/, "settings migration must record the known owner of legacy workspace data once");
assert.match(
  stateSource,
  /normalizeStoredSettings\(\);/,
  "settings must be normalized once on startup",
);
assert.match(
  stateSource,
  /function normalizeStoredSettings\(\): void/,
  "settings normalization helper must exist",
);
assert.match(
  stateSource,
  /copyFileSync\(this\.filePath, backupPath\)/,
  "corrupt settings files must be backed up before reset",
);
assert.match(
  stateSource,
  /this\.recoveryNotice = backupPath;/,
  "corrupt settings recovery must retain the backup path for user notification",
);
assert.match(
  stateSource,
  /consumeRecoveryNotice\(\): string \| null/,
  "settings store must expose a one-shot recovery notice",
);
assert.match(
  stateSource,
  /export function consumeSettingsRecoveryNotice\(\): string \| null/,
  "main process must be able to consume the settings recovery notice",
);
assert.match(
  stateSource,
  /settingsStore\.setMany\(\{\s+settingsSchemaVersion: CURRENT_SETTINGS_SCHEMA_VERSION/s,
  "startup settings normalization must be batched into one write",
);
assert.match(
  stateSource,
  /\.invalid-\$\{new Date\(\)\.toISOString\(\)\.replace/,
  "corrupt settings backups must use a timestamped invalid suffix",
);
assert.match(
  mainSource,
  /showSettingsRecoveryNotice\(\);/,
  "desktop startup must surface settings recovery to the user",
);
assert.match(
  mainSource,
  /function showSettingsRecoveryNotice\(\): void/,
  "settings recovery startup notifier must exist",
);
assert.match(
  mainSource,
  /consumeSettingsRecoveryNotice\(\)/,
  "settings recovery notification must be one-shot",
);
assert.match(
  mainSource,
  /showTaskNotification\("TaskBridge", body\)/,
  "settings recovery must use a user-visible desktop notification",
);

assert.match(
  appSource,
  /async function reloadTasksAndPruneReminders\(\): Promise<void>/,
  "task reloads must prune reminder dedupe state",
);
assert.match(
  appSource,
  /await syncStore\.start\(reloadTasksAndPruneReminders\)/,
  "sync callbacks must reload tasks through reminder pruning",
);
assert.match(
  appSource,
  /function pruneNotifiedReminders\(\): void/,
  "app must prune stale reminder dedupe keys",
);
assert.match(
  appSource,
  /notifiedReminderIds\.clear\(\);/,
  "logout must clear reminder dedupe state",
);
assert.match(
  appSource,
  /function getReminderDedupKey\(/,
  "reminder dedupe keys must be generated by a single helper",
);
assert.match(
  dbSource,
  /CREATE INDEX IF NOT EXISTS ix_tasks_active_timeline/,
  "desktop task storage must include a dedicated active timeline index",
);
assert.match(
  dbSource,
  /CREATE INDEX IF NOT EXISTS ix_tasks_active_reminders/,
  "desktop task storage must include a reminder lookup index",
);
assert.match(
  dbSource,
  /updated_at DESC,\s+local_id ASC/,
  "desktop task ordering must be deterministic across equal timestamps",
);
const diagnosticsDetailsTag = extractOpeningTag(
  syncRecoveryPanelSource,
  '<details class="settings-advanced-details"',
);
assert.match(diagnosticsDetailsTag, /:open="diagnosticsOpen"/, "desktop sync diagnostics must bind its disclosure state");
assert.equal(hasLiteralBooleanAttribute(diagnosticsDetailsTag, "open"), false, "desktop sync diagnostics must not be open by default");
assert.match(syncRecoveryPanelSource, /<summary>\{\{ settingsStore\.t\("settings\.syncDiagnostics"\) \}\}<\/summary>[\s\S]*?<div class="sync-diagnostics">/, "desktop sync diagnostics must be hidden behind an advanced details disclosure");
assert.match(
  settingsViewSource,
  /function timeZoneOptionLabel/,
  "desktop timezone choices must use a user-facing label helper",
);
assert.doesNotMatch(
  settingsViewSource,
  /\{\{\s*settingsStore\.language === "zh-CN" \? zone\.zh : zone\.en\s*\}\}\s*·\s*\{\{\s*zone\.value\s*\}\}/,
  "desktop timezone choices must not expose raw IANA identifiers in the primary option text",
);

console.log("desktop efficiency config check passed");
